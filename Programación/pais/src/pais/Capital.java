package pais;

public class Capital {
	private String capital;
	private int habitantesCapital;

	public Capital() {
	}

	public String getCapital() {
		return capital;
	}

	public void setCapital(String capital) {
		this.capital = capital;
	}

	public int getHabitantesCapital() {
		return habitantesCapital;
	}

	public void setHabitantesCapital(int habitantesCapital) {
		this.habitantesCapital = habitantesCapital;
	}
}